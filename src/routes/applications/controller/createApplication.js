const db = require('../../../db/index');

const createApplication = (req, res) => {
  let { dateOpen, dateClosed, dateResponse } = req.body;

  // Convert the string date values into Date objects.
  // Firebase will handle the rest.
  let [date_open_date, date_closed_date, date_response_date] = [
    new Date(dateOpen),
    new Date(dateClosed),
    new Date(dateResponse),
  ];

  let newBody = {
    dateOpen: date_open_date,
    dateClosed: date_closed_date,
    dateResponse: date_response_date,
    ...req.body,
  };

  db.collection('cohorts')
    // add a new application with id generated by Firestore
    .add({ ...newBody })
    .then(docRef => {
      console.log('doc', docRef);
      res.status(201).json({
        id: docRef.id,
        message: 'Application successfully added to the database!',
      });
    })
    .catch(error => {
      res.json({ error });
    });
};

module.exports = {
  createApplication,
};
